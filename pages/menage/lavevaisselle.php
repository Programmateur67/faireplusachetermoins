                        <div class="card-body ">
                            <p class="card-text ">Je fais ce produit en petite quantité dans un bocal à large col de 750cl, parce qu’il a tendance à durcir, et quand on est en panne, c’est tellement rapide, que j’en refais sur le champs. C’est la recette intégrale du (supertop) livre « La famille (presque) zéro déchet ».</p>
                            <p class="card-text ">Dans mon bocal, je verse 4 cuillerées à soupe de percarbonate, puis 8 cuillères à soupe de cristaux de soude, je mélange avec la grande cuillère. J’ajoute 8 cuillères (toujours à soupe) de paillettes de savon de Marseille, je mélange. Et enfin 8 cuillères d’acide citrique et je mélange et je ferme hermétiquement sans perdre de temps.</p>
                            <p class="card-text ">Pour faire tourner mon lave-vaisselle, je mets une bonne cuillère à soupe de cette poudre dans le compartiment dédié au lavage. Si le mélange s’est durci, je gratouillle un peu avec la cuillère pour le faire revenir en poudre, et ça marche très bien.</p>
                            <p class="card-text ">Le <span class="fa fa-plus-circle "></span> C’est magique, on n’a jamais eu une vaisselle qui sortait aussi propre du lave-vaisselle ! Les traces de thé sur les vieilles tasses, les marques de calcaire sur les verres etc: tout a disparu. D’ailleurs c’était l’une des initiatives que craignait le plus Jean-Christophe et qui l’a complètement convaincu, il n’en revenait pas.</p>
                            <p class="card-text ">Le <span class="fa fa-minus-circle "></span> Assez rapidement, j’ai constaté un dépôt gras sur le filtre et le joint du lave-vaisselle, largement décrit par d’autres utilisateurs sur les réseaux. C’est lié à la présence du savon de Marseille. J'essuie régulièrement le dépôt et je lave le filtre, qui reste un peu graisseux malgré tout. Pour améliorer, j’essaie en ce moment un autre savon de Marseille, sans glycérine (Marius Fabre, pour le citer, qui est l’un des rares à avoir l’appellation savon de Marseille véritable): on verra si c’est mieux. Sinon, j’essayerai peut-être une recette sans savon de Marseille mais pour l’instant le bénéfice me semble de toute façon largement supérieur aux contraintes.</p>
                            <div class="card">
                                <div class="card-body">
                                    <p class="card-text ">Liquide rinçage: alors là, ça fait des années que je fais ça plutôt que d’acheter le produit industriel: je le remplace purement et simplement par du vinaigre d’alcool blanc (je prends celui à 12° d’acidité: déjà essayé avec celui à 8°, c’est moins puissant). C’est bête comme chou et super efficace (et non, ça ne sent pas le vinaigre quand ça sort du lave-vaisselle).</p>
                                </div>
