                        <div class="card-body ">
                            <p class="card-text">Dans une carafe en verre qui ferme, je verse 75cl de vinaigre blanc et je mets à tremper les peaux de citron ou d’orange après les avoir pressées pour la cuisine (en gros, je fais la recette quand j’en ai sous la main, d’autant que les agrumes ne sont pas les bienvenus en quantité dans le compost).</p>
                            <p class="card-text">Pour la quantité, c’est du pif à chaque fois, et je ne pense pas que ce soit très important mais à mon avis il faut au moins deux fruits en peaux pour bien parfumer. Je laisse macérer au moins 48h, puis je filtre et mets en bouteille dans une ancienne bouteille de limonade en verre. C’est prêt!</p>
                            <p class="card-text">C’est le produit que j’utilise pour nettoyer ma plaque de cuisson en inox: un petit peu de vinaigre aromatisé sur une éponge, je passe partout pour dégraisser, puis un coup d’éponge rincée pour rincer, puis un coup de chiffon pour essuyer les traces. C’est vraiment pas la peine d’acheter toutes sortes de produits miracles ou lingettes je-ne-sais-quoi…</p>
                            <p class="card-text">Ce vinaigre me sert aussi à nettoyer la porte du lave-vaisselle, le frigo, les poignées de porte et toutes sortes de surfaces. Pratique aussi pour dégraisser une poêle à frire bien grasse.</p>
                            <p class="card-text">On peut bien évidemment passer du vinaigre sans s’embêter à l’aromatiser. Là, c’est juste une façon de masquer un peu l’odeur forte du vinaigre. Et avec le citron, il y a certainement un intérêt antiseptique supplémentaire.</p>
                            <p class="card-text">Pour la plaque de cuisson, si c’est plus grave que juste de l’entretien courant, qu’il y a des restes collés, des traces de brûlé etc, <a class="text-muted" href="../../pages/menage/recurerlaplaqueinox.php">je passe la vitesse supérieure.</a></p>
