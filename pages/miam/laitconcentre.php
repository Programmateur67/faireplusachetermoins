                        <div class="card-body ">
                            <p class="card-text">Dans une casserole bien large type sauteuse, je verse le lait et le sucre et je mets à cuire à feu vif en remuant avec une cuillère en bois.</p>
                            <p class="card-text">Je surveille très attentivement le point d’ébullition du lait pour éviter une éruption. Dès qu’il arrive, et avant que le lait ne monte, je baisse sur feu doux, pour maintenir un continuel frémissement. Je remue régulièrement.</p>
                            <p class="card-text">Alors oui, c’est très long et passablement ennuyeux, car il faut attendre que le lait ait perdu une bonne partie de son eau (grosso modo, une heure tout de même). Dès que la préparation s’épaissit et prend une couleur un peu jaune (un peu plus que celui du commerce), le lait concentré est prêt.</p>
                            <p class="card-text">Si on laisse le lait un peu plus longtemps, on observe une couleur de plus en plus caramel. Ce n’est pas grave, mais on s’approche alors plus de la confiture de lait, délicieuse, qui peut s’utiliser notamment en pâte à tartiner ou dans les crêpes. La consistance sera moins liquide que le lait concentré mais la préparation se conservera plus longtemps.</p>
                            <p class="card-text">Je transvase mon lait concentré dans deux pots à confiture bien propres et je le conserve au frigo plusieurs semaines.</p>
                            <p class="card-text">Je ferai des essais pour le préparer en plus grande quantité et le stériliser.</p>
                            <p class="card-text">Quelle joie de pouvoir zapper la marque hégémonique qui le commercialise dans la grande distribution! Et en plus, celui-ci conserve un goût de lait plus prononcé, c’est bien plus savoureux (pour ceux qui supportent tant de sucre). Une base idéale pour la pâtisserie comme le « sablé millionnaire ». Ou à déguster tel quel, comme aime le faire mon troisième garçon, qui le mélange à du quinoa soufflé au petit déjeuner.</p>
