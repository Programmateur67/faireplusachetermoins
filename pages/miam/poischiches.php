                        <div class="card-body ">
                            <p class="card-text">La veille, je mets à tremper les pois chiches dans un grand récipient d’eau avec une cuillère de bicarbonate de soude (pour en améliorer la digestion).</p>
                            <p class="card-text">Le jour même, je rince bien les pois chiches.</p>
                            <p class="card-text">Je les fais cuire une bonne heure dans une grande casserole d’eau bouillante non salée (très important). Je goûte pour être sûre qu’ils soient cuits (et je vérifie aussi qu’ils ne partent pas en purée, ce qui est très bon mais pas pour cette recette)</p>
                            <p class="card-text">Je préchauffe le four à 180°C chaleur tournante.</p>
                            <p class="card-text">Je pèle l’ail et l’émince en tout petit ou le presse, dans un saladier, avec l’huile d’olive, le sel (une cuillère à soupe) et le poivre (dix tours de moulin, selon les goûts).</p>
                            <p class="card-text">J’huile une plaque de four à l’huile d’olive et je la parsème de sel (euh, en fait, il faudra peut-être deux plaques).</p>
                            <p class="card-text">Attention, lorsque j’égoutte les pois chiches, je garde précieusement leur eau de cuisson (eh oui, c’est un équivalent du blanc d’œuf, l’occasion de faire par exemple meringues ou macarons dans la foulée pour profiter de tout ce que nous offrent ces merveilleux pois chiches, recettes à venir).</p>
                            <p class="card-text">Je verse les pois chiches égouttés dans le saladier avec l’assaisonnement, et je remue bien pour qu’ils soient tous imprégnés.</p>
                            <p class="card-text">Je verse les pois chiches dans la ou les plaques de façon à ce qu’ils ne se chevauchent pas et j’enfourne pour une heure environ (attention, il faut surveiller et remuer tous les quarts d’heure).</p>
                            <p class="card-text">Si je mets deux plaques, j’intervertis leurs positions, de façon à ce qu’elles fassent toutes les deux un séjour en position gril. Les pois ne doivent pas noircir mais prendre une couleur caramel et devenir durs. La première fois, je les ai sortis trop tôt: ils étaient encore un peu mous. Mais je les ai remis le lendemain pour dix minutes et c’était top. Ils doivent être craquants comme des petits pois au wasabi...</p>
                            <p class="card-text">Et d’ailleurs... je vais réessayer la recette en changeant l’assaisonnement, en ajoutant du cumin pour un goût plus oriental, voire du piment. Et me lancer aussi dans le petit pois au wasabi maison, youpi!</p>
                            <p class="card-text">Une fois refroidis, je conserve ces apéros à picorer dans une boîte en inox ou un bocal en verre. A mon avis, ça se garde longtemps mais je ne peux pas tester parce que c’est addictif.</p>
