                        <div class="card-body ">
                            <p class="card-text">Pour éviter le plastique et les cartouches suremballées des carafes filtrantes du commerce, j’ai remplacé celles-ci par un cubi en verre et <a href="http://www.sans-bpa.com/black-blum/1924-charbon-actif-binchotan-black-blum-0732396214043.html" class="text-muted">du charbon binchotan. </a></p>
                            <p class="card-text">Efficace six mois, le charbon, sous forme de bâtonnets d’une dizaine de centimètres, doit être « rechargé » en milieu de course, soit au bout de trois mois. Pour cela, on le fait bouillir pour le purifier puis on le sèche à l’air libre (au soleil, encore mieux).</p>
                            <p class="card-text">Au bout de six mois, on peut s’en servir pour désodoriser les chaussons d’escalade (ou les chaussures bien sûr), fertiliser les plantes ou enrichir le compost. Il est donc presque zéro déchet, mais pas tout à fait puisqu’il est vendu sous vide (je ne l’ai trouvé pour l’instant que par correspondance, mais de toute façon il vient du Japon -oui, ce n’est pas un truc très locavore non plus).</p>
                            <p class="card-text">Le principe: chaque soir, on remplit notre cubi dans lequel on a placé les charbons avec 5 litres d’eau. Au bout de quatre heures, la filtration sera 100% efficace, donc ça permet d’avoir directement l’eau disponible le matin.</p>
                            <p class="card-text">Si on n’a pas tout bu dans la journée, je vide le reste dans la bouilloire ou dans une bouteille pour repartir de zéro sans la gâcher -il est aussi recommandé de ne pas laisser l’eau en contact direct avec le charbon plus de 48 heures.</p>
                            <p class="card-text">Le goût est vraiment bon (et meilleur qu’avec la carafe filtrante du commerce). En plus, ce charbon actif végétal a des propriétés intéressantes pour capter les poisons (malheureusement pas tous: le souci écologique et sanitaire de l’eau ne trouve pas de réponse entièrement satisfaisante pour l’instant, ce qui, quand on y réfléchit, est vraiment un scandale, parce que l’eau, c’est vraiment la vie…).</p>
